#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*t;

	while (lst && *lst)
	{
		t = (*lst)->next;
		(*del)((*lst)->content);
		free(*lst);
		*lst = t;
	}
}
